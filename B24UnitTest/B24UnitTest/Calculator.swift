import Foundation

class Calculator {
  
  func add(num1: Int, num2: Int) -> Int {
    let result = num1 + num2
    return result
  }
  
  func subtract(num1: Int, num2: Int) -> Int {
    let result = num1 - num2
    return result
  }
  
  func multiply(num1: Int, num2: Int) -> Int {
    let result = num1 * num2
    return result
  }
  
  func divide(num1: Int, num2: Int) -> Int {
    let result = num1 / num2
    return result
  }
  
}
